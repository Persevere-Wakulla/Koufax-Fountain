# f strings are optinal methods that give users more control when displaying output

number = 1000

print(f"The number is {number:,}") #add comma to all 1 thousanths places
print(f"The number is {number:b}") #binary representation
print(f"The number is {number:o}") #octodecimal
print(f"The number is {number:x}") #hexadecimal
print(f"The number is {number:E}") #scientific notation
